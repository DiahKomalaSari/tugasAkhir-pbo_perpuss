    void isitabel(){
    Object data[][] = new Object[record.size()][8];
    int x = 0;
    for (ModelDataAnggota ktm : record){
    data[x][0]=ktm.getId_anggota();
    data[x][1]=ktm.getNpm();
    data[x][2]=ktm.getNama();
    data[x][3]=ktm.getJurusan();
    data[x][4]=ktm.getJenis_kelamin();
    data[x][5]=ktm.getEmail();
    data[x][6]=ktm.getNo_telp();
    data[x][7]=ktm.getUmur();
    data[x][8]=ktm.getAlamat();
    x++;
    }
    String judul[]={"Id","NPM","Nama","Jurusan","JK","Email","No.telp","Umur","Alamat"};
    jTable1.setModel(new DefaultTableModel(data,judul));
    }
    
