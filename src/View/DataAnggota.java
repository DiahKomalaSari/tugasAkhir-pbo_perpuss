    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
  String id_anggota=jTextField7.getText();
  String npm=jTextField1.getText();
  String nama=jTextField2.getText();
  String jurusan=(String) jComboBox1.getSelectedItem();
  String jk=jRadioButton1.getText();
  String jk1=jRadioButton2.getText();
  String email=jTextField3.getText();
  String no=jTextField4.getText();
  String umur=jTextField5.getText();
  String alamat=jTextArea1.getText();
        if(id_anggota.trim().equals("") && npm.trim().equals("")&& nama.trim().equals("")&& jurusan.trim().equals("")&& jk.trim().equals("")&& jk1.trim().equals("")&& email.trim().equals("")&& no.trim().equals("")&& umur.trim().equals("")&& alamat.trim().equals("")){
            JOptionPane.showMessageDialog(this,"Data Tidak Boleh Kosong");
        }else{
            try {
                ModelDataAnggota perpus=new ModelDataAnggota(null, null);
                perpus.setId_anggota(jTextField7.getText());
                perpus.setNpm(jTextField1.getText());
                perpus.setNama(jTextField2.getText());
                perpus.setJurusan ((String) jComboBox1.getSelectedItem());
                perpus.setJenis_kelamin(jRadioButton1.getText());
                perpus.setJenis_kelamin(jRadioButton2.getText());
                perpus.setEmail(jTextField3.getText());
                perpus.setNo_telp(jTextField4.getText());
                perpus.setUmur(jTextField5.getText());
                perpus.setAlamat(jTextArea1.getText());
                perpusservis.simpan(perpus);
                JOptionPane.showMessageDialog(this,"Data Telah Berhasil di Simpan");
                
            } catch (SQLException o) {
                System.out.println("gagal mengedit"+o);
                
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

